// после чего попросить ввести те же числа с помощью `prompt()` по памяти, если все числа совпали,
// то выводим `alert()` с поздравлениям, если не совпало хотя бы одно число, сразу выводим `alert()` с ошибкой

const arrNumber = [3, 5, 2, 6, 8, 1, 7];

alert(`Вам даны числа, запомните их: ${arrNumber}`);
const userInput = prompt("Введите эти числа через запятую:");
let userNumber = userInput.split(",");

for (let i = 0; i < arrNumber.length; i++) {
  if (Number(userNumber[i]) !== arrNumber[i]) {
    alert("ошибка!");
    break;
  }

  if (i === arrNumber.length - 1) {
    alert("Поздравляем! Вы ввели правильные числа!");
  }
}
